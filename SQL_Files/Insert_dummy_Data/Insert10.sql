CREATE TEMPORARY TABLE if not exists temp_result AS
SELECT 
    p2.Price,
    p2.Name,
    p2.SupplierID,
    s2.Name AS SupplierName,
    i2.Description,
    p2.ProductID
FROM 
    Product AS p2
JOIN 
    Supplier AS s2 ON p2.SupplierID = s2.SupplierID
JOIN 
    Product_Info AS i2 ON i2.ProductID = p2.ProductID;
    
CREATE TEMPORARY TABLE if not exists temp1_result AS
SELECT 
    p2.Price,
    p2.Name,
    p2.SupplierID,
    s2.Name AS SupplierName,
    i2.Description,
    p2.ProductID
FROM 
    Product AS p2
JOIN 
    Supplier AS s2 ON p2.SupplierID = s2.SupplierID
JOIN 
    Product_Info AS i2 ON i2.ProductID = p2.ProductID where  p2.ProductID = 10;

